# homework_4
arr = [1, 2, 3, 3, 6, 1, 6]
new_arr = []

for i in arr:  # перебираем исходный массив в цикле for
    if i not in new_arr:  # проверяем есть ли символ исходного массива в новом массиве
        if arr.count(i) >= 2:  # проверяем встречаются ли символы более 2 раз
            new_arr.append(i)  # добавляем встречающиеся более двух раз в новый массив
print(f"Новый массив: {new_arr}")  # выводим новый массив
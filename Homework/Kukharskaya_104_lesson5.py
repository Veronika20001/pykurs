arr = [2, 8, 6, 7, 1]  # задаём переменной arr список
total_1 = 0  # 0 не будет влиять на сумму при конкатенации
total_2 = 1  # 1 не будет влиять на произведение при умножении строк
for i in arr:  # задаём цикл for
    total_2 *= i  # умножение строк
    total_1 += i  # конкатенация
print(f'Произведение {total_2}\nСумма {total_1}')  # выводим, полученные данные, используя f-строку

